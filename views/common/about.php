    	<h5>History</h5>
        <p>Paige was developed in early 2012 by <a href="http://mbmccormick.com" target="_blank">Matt McCormick</a> and <a href="http://twitter.com/j_martella" target="_blank">Joe Martella</a> during a 24-hour Hackathon event at Purdue University. After a long night of caffeine-fueled coding and collaboration, Paige was born. Our team went on to win first place in the competition and the service received rave reviews from professors in the Computer Science department.</p>
        <h5>How it works</h5>
		<p>Paige makes it easy to outsource your business's personnel paging needs. In the past, companies had to pay costly fees to run their pager fleet, staff their answering service, and manually escalate cases to other on-call personnel. Paige streamlines this process by moving everything to the web.</p>
		<p>When you sign up for Paige, a phone number is registered for your new paging service. This number is used to page your team members when an incident arises. You can schedule your on-call team member within our application and the system will automatically page the person on-call.</p>
		<p>If a team member misses a page, our service will automatically retry after 10 minutes. If they miss the second attempt, a third attempt is made after 10 minutes. If the on-call team member misses the third page attempt, Paige will notify the whole team of this incident.
		<h5>Paging your team</h5>
		<p>Paige makes it easy to notify your team of an incident. There are three ways to do this:
		<ul>
			<li>Send a message from the web application</li>
			<li>Call your account phone number and follow the prompts</li>
			<li>Integrate your secret URL with existing monitoring services</li>
		</ul>
		Integrating with Paige couldn't be easier. Each account has a secret "page URL" that can be used to page your team from existing monitoring tools your business already uses. With one simple HTTP GET request, your team will be paged instantly.</p>
