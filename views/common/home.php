        <h3>Web-based personnel paging made easy</h3>
        <p style="line-height: 1.5em;">Paige makes it easy to outsource your business’s personnel paging needs. In the past, companies had to pay costly fees to run their pager fleet, staff their answering service, and manually escalate cases to other on-call personnel. Paige streamlines this process by moving everything to the web.</p>
        <p style="line-height: 1.5em;">When you sign up for Paige, a phone number is registered for your new paging service. This number is used to page your team members when an incident arises. You can schedule your on-call team member within our application and the system will automatically page the person on-call.</p>
        <p style="line-height: 1.5em;">If a team member misses a page, our service will automatically retry after 10 minutes. If they miss the second attempt, a third attempt is made after 10 minutes. If the on-call team member misses the third page attempt, Paige will notify the whole team of this incident.</p>
        <p style="line-height: 1.5em;">You can page your team through Paige’s web interface, your private telephone menu, or through our simple API.</p>
